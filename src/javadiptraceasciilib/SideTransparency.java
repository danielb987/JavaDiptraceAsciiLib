package javadiptraceasciilib;

/**
 * Should the other side of the PCB be visible?
 */
public enum SideTransparency {
    
    /**
     * Don't show the opposite side.
     */
    NONE,
    
    /**
     * Show the opposite side with some transparency.
     */
    PART,
    
    /**
     * Show the other side clear.
     */
    FULL

}
